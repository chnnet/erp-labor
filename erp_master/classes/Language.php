    public static function get(string $key, array $vars = []): string {
        $lang = $_SESSION['language'] ?? 'de';
        $text = self::$texts[$lang][$key] ?? self::$texts['de'][$key] ?? $key;

        foreach ($vars as $placeholder => $value) {
            $text = str_replace("{" . $placeholder . "}", $value, $text);
        }

        return $text;
    }
